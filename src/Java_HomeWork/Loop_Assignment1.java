package Java_HomeWork;

public class Loop_Assignment1 {

	public static void main(String[] args) 
	{
		System.out.println("using a for loop");

		for(int i=1;i<=5;i++)
		{
			System.out.println("I am Batman");
		}

		System.out.println("Using a while loop");
		int j=1;

		while(j<=5)
		{
			System.out.println("I am Batman");
			j++;
		}

	}

}
